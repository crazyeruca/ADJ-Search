std::string GBKToUTF8(const std::string& strGBK)
{
	std::string strOutUTF8 = "";
	WCHAR * str1;
	int n = MultiByteToWideChar(CP_ACP, 0, strGBK.c_str(), -1, NULL, 0);
	str1 = new WCHAR[n];
	MultiByteToWideChar(CP_ACP, 0, strGBK.c_str(), -1, str1, n);
	n = WideCharToMultiByte(CP_UTF8, 0, str1, -1, NULL, 0, NULL, NULL);
	char * str2 = new char[n];
	WideCharToMultiByte(CP_UTF8, 0, str1, -1, str2, n, NULL, NULL);
	strOutUTF8 = str2;
	delete[]str1;
	str1 = NULL;
	delete[]str2;
	str2 = NULL;
	return strOutUTF8;
}

BOOL FolderExist(std::string strPath)
{
	WIN32_FIND_DATA   wfd;
	BOOL rValue = FALSE;
	HANDLE hFind = FindFirstFile(strPath.c_str(), &wfd);
	if ((hFind != INVALID_HANDLE_VALUE) && (wfd.dwFileAttributes & FILE_ATTRIBUTE_DIRECTORY))
	{
		rValue = TRUE;
	}
	FindClose(hFind);
	return rValue;
}

void SimpleIndex() {
	TCHAR *path = new TCHAR[MAX_PATH];
	ZeroMemory(path, MAX_PATH);
	// path == "d:\Project\Test\MFC\MFC\debug"
	GetCurrentDirectory(MAX_PATH, path);

	std::string strPath(path);
	if (FolderExist(strPath + "//index_data"))
	{
		return;
	}

	// Open the database for update, creating a new database if necessary.
	Xapian::WritableDatabase db("./index_data", Xapian::DB_CREATE_OR_OPEN);

	Xapian::TermGenerator indexer;
	Xapian::Stem stemmer("english");
	indexer.set_stemmer(stemmer);

	int nFlag = 0;
	std::string src;
	while (nFlag < 5)
	{
		if (0 == nFlag)
		{
			src = "我是一个芒果";
		}
		else if (1 == nFlag)
		{
			src = "我是一个香蕉";
		}
		else if (2 == nFlag)
		{
			src = "我爱祖国天安门";
		}
		else if (3 == nFlag)
		{
			src = "天安门上太阳照";
		}
		else if (4 == nFlag)
		{
			src = "指引我们向前进";
		}

		Xapian::Document doc;

		doc.set_data(GBKToUTF8(src));
		indexer.set_document(doc);
		indexer.index_text(GBKToUTF8(src));

		db.add_document(doc);

		nFlag++;
	}

	// Explicitly commit so that we get to see any errors.  WritableDatabase's
	// destructor will commit implicitly (unless we're in a transaction) but
	// will swallow any exceptions produced.
	db.commit();
}


